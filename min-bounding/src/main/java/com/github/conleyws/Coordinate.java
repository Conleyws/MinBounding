package com.github.conleyws;

/**
 * 
 * @author conleyws
 */
public class Coordinate {
    Double x;
    Double y;

    public Coordinate(Double x, Double y) {
        this.x = x;
        this.y = y;
    }

    public Double getX() {
        return this.x;
    }

    public Double getY() {
        return this.y;
    }
}
